//Math Object

//PI
console.log(Math.PI);

//Math round
console.log(Math.round(3.49));
console.log(Math.round(3.5));

//pow - exponents
console.log(Math.pow(3,3));
console.log(Math.pow(3,4));

//abs - absolute value - returns positve always
console.log(Math.abs(10));
console.log(Math.abs(-130));

//ceil - rounds up always
console.log(Math.ceil(3.001));

//floor - rounds down always
console.log(Math.floor(3.999999));

//MIN & MAX
console.log(Math.min(12, 42));
console.log(Math.max(12, 42));

//random - returns a "RANDOM" number from 0-1
console.log(Math.random());
console.log(Math.floor(Math.random()*50000));
